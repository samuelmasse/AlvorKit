namespace AlvorKit.Windowing.Test;

[TestClass]
public class AgentWindowCommandRunnerTest
{
    /// <summary>Verifies that interactive command lines drive exact updates, input, render, and inspection.</summary>
    [TestMethod]
    public void AgentWindowCommandRunner_Commands_DriveHost()
    {
        var host = CreateAgent();
        var loop = new WindowLoop(host);
        var mouse = new Mouse(loop);
        var keyboard = new Keyboard(loop);
        using var output = new StringWriter(CultureInfo.InvariantCulture);
        var runner = new AgentWindowCommandRunner(host, output);

        Assert.IsTrue(runner.Execute("   "));
        runner.Execute("update 0.25");
        runner.Execute("move 10 20");
        runner.Execute("key Space down");
        runner.Execute("updates 2 0.5 3 4");
        runner.Execute("render 0.125");
        runner.Execute("clipboard hello");
        runner.Execute("text ok");
        runner.Execute("state");

        Assert.AreEqual(new Vec2(16, 28), mouse.Position);
        Assert.IsTrue(keyboard.IsKeyDown(Keys.Space));
        Assert.AreEqual(1.25, host.Time);
        Assert.AreEqual(3, host.UpdateCount);
        Assert.AreEqual(1, host.RenderCount);
        Assert.AreEqual("hello", host.Clipboard);
        StringAssert.Contains(output.ToString(), "updates=3");
    }

    /// <summary>Verifies the remaining interactive commands and the reader loop.</summary>
    [TestMethod]
    public void AgentWindowCommandRunner_Run_HandlesScript()
    {
        var host = CreateAgent();
        var loop = new WindowLoop(host);
        var mouse = new Mouse(loop);
        var keyboard = new Keyboard(loop);
        using var output = new StringWriter(CultureInfo.InvariantCulture);
        using var input = new StringReader("""
            step 0.25
            mouse Left down
            mouse Left up
            key A repeat
            key A up
            pan 2 3
            wheel 0 -1
            resize 40 50
            focus false
            visible false
            close
            exit
            """);
        var runner = new AgentWindowCommandRunner(host, output);

        runner.Run(input);

        Assert.AreEqual(0.25, host.Time);
        Assert.AreEqual(1, host.UpdateCount);
        Assert.AreEqual(1, host.RenderCount);
        Assert.IsTrue(mouse.IsMainUp());
        Assert.IsTrue(keyboard.IsKeyPressedRepeated(Keys.A));
        Assert.AreEqual(new Vec2(2, 3), mouse.Position);
        Assert.AreEqual(new Vec2(0, -1), mouse.Wheel);
        Assert.AreEqual(new Vec2u(40u, 50u), host.ClientSize);
        Assert.IsFalse(host.IsFocused);
        Assert.IsFalse(host.IsVisible);
        Assert.IsTrue(host.IsExiting);
    }

    /// <summary>Verifies that malformed script commands report errors and keep the reader loop alive.</summary>
    [TestMethod]
    public void AgentWindowCommandRunner_Run_InvalidCommandReportsErrorAndContinues()
    {
        var host = CreateAgent();
        using var output = new StringWriter(CultureInfo.InvariantCulture);
        using var input = new StringReader("""
            update
            focus maybe
            state
            exit
            """);
        var runner = new AgentWindowCommandRunner(host, output);

        runner.Run(input);

        var text = output.ToString();
        StringAssert.Contains(text, AgentWindowCommandRunner.CommandErrorPrefix + "Required argument missing for command: 'update'.");
        StringAssert.Contains(text, AgentWindowCommandRunner.CommandErrorPrefix + "Cannot parse argument 'maybe' for command 'focus'");
        StringAssert.Contains(text, "updates=0");
    }

