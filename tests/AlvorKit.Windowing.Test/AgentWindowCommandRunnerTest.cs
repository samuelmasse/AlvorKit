    /// <summary>Verifies that malformed command arguments fail with useful exceptions.</summary>
    [TestMethod]
    public void AgentWindowCommandRunner_InvalidArguments_Throw()
    {
        var host = CreateAgent();
        using var output = new StringWriter(CultureInfo.InvariantCulture);
        var runner = new AgentWindowCommandRunner(host, output);

        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("update"));
        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("update -1"));
        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("update 1 2"));
        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("update nope"));
        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("update 1 NaN 2"));
        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("update 1 nope 2"));
        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("updates"));
        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("updates nope 1"));
        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("updates -1 1"));
        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("render -1"));
        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("render nope"));
        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("move"));
        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("move nope 1"));
        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("move 1"));
        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("move 1e100 1"));
        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("click"));
        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("click nope 1"));
        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("click 1 2 -1"));
        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("drag 1 2 3 4 0 0.016"));
        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("drag 1 2 3 4 1 NaN"));
        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("key"));
        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("key A sideways"));
        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("key 999 down"));
        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("mouse Left sideways"));
        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("mouse 999 down"));
        Assert.ThrowsException<InvalidOperationException>(() => runner.Execute("text"));
    }

    private AgentGlfwWindowHost CreateAgent()
    {
        var size = new Vec2u(800, 600);
        var glfw = new WindowingTestGlfw(size);
        return new(glfw, glfw.Window, new(new GlNoop()), size);
    }
